int	ft_is_valid_nbr(char *str)
{
	int	i;

	i = 0;
	while ('0' <= str[i] && str[i] <= '9')
		i++;
	if (i == 0)
		return (0);
	else
		return (str[i] == '\0' || (str[i] == '\n' && str[i + 1] == '\0'));
}
